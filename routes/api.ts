// Your custom routes go here:
route.get('/', () => response.text('hello world'))

// Monitor CRUD (index/show/update/destroy) is auto-generated by the
// `useApi` trait on app/Models/Monitor.ts. `store` is overridden here
// (user-defined routes take priority over the auto-generated ones) to
// enforce the team's plan limit before creating — see
// Actions/Monitors/CreateMonitorAction. Custom action: triggering an
// out-of-band check outside the scheduler's normal cadence.
route.post('/monitors', 'Actions/Monitors/CreateMonitorAction')
route.post('/monitors/{id}/check', 'Actions/Monitors/RunCheckAction')

// Incident CRUD (index/store/show/update) is auto-generated by the
// `useApi` trait on app/Models/Incident.ts. Custom action: a one-step
// acknowledge, since the auto-generated `update` route requires PATCHing
// the full resource with a status string.
route.post('/incidents/{id}/acknowledge', 'Actions/Incidents/AcknowledgeIncidentAction')

// Public heartbeat ping endpoint (no auth — the token itself is the secret).
// GET *and* POST both work since cron jobs commonly just `curl` the URL.
route.get('/ping/{token}', 'Actions/Heartbeats/ReceivePingAction')
route.post('/ping/{token}', 'Actions/Heartbeats/ReceivePingAction')

// Status page subscriber signup/unsubscribe — public, no auth.
route.post('/status/{slug}/subscribe', 'Actions/StatusPages/SubscribeAction')
route.get('/status/{slug}/unsubscribe/{token}', 'Actions/StatusPages/UnsubscribeAction')
route.get('/status/{slug}/feed', 'Actions/StatusPages/IncidentFeedAction')

