route.post('/team-forms/{id}/invite', 'Actions/Teams/DashboardInviteTeamMemberAction')
route.post('/team-forms/{id}/remove', 'Actions/Teams/DashboardRemoveTeamMemberAction')

// Security settings form posts (stacksjs/status#1 Phase 9 follow-up —
// the passkey/2FA dashboard UI deferred until real dashboard auth
// existed). Same plain-POST convention; each action resolves the
// requester from their real credential (resolveAuthenticatedUser) since
// native form posts carry only the login cookie, no bearer header.
route.post('/security-forms/two-factor/enable', 'Actions/Security/DashboardEnableTwoFactorAction')
route.post('/security-forms/two-factor/disable', 'Actions/Security/DashboardDisableTwoFactorAction')
route.post('/security-forms/passkeys/delete', 'Actions/Security/DashboardDeletePasskeyAction')

// Passkey (WebAuthn) enrollment + passwordless sign-in (stacksjs/status#1
// Phase 9 follow-up). Enrollment resolves the signed-in user from the
// auth-token cookie (NOT the bearer/session Auth middleware, which a
// dashboard fetch can't satisfy); sign-in is unauthenticated and mints a
// session on success. Verification is app-owned and spec-correct — see
// Actions/Security/webauthn.ts for why it doesn't use ts-auth's verifiers.
// All four are CSRF-protected (the client sends the double-submit token).
route.post('/passkeys/register/options', 'Actions/Security/PasskeyRegisterOptionsAction')
route.post('/passkeys/register/verify', 'Actions/Security/PasskeyRegisterVerifyAction')
route.post('/passkeys/login/options', 'Actions/Security/PasskeyLoginOptionsAction')
route.post('/passkeys/login/verify', 'Actions/Security/PasskeyLoginVerifyAction')

// Billing (stacksjs/status#1 Phase 9). /checkout is a dashboard form
// post, same convention as the rest of this block. /webhook is called
// by Stripe itself — no CSRF token to satisfy (skipCsrf, matching the
// storefront/contact-form precedent above), and its own signature
// verification (StripeWebhookAction) is the real security control.
route.post('/billing-forms/checkout', 'Actions/Billing/CreateCheckoutSessionAction')
route.post('/billing-forms/cancel', 'Actions/Billing/CancelSubscriptionAction')
route.post('/billing-forms/webhook', 'Actions/Billing/StripeWebhookAction').name('billing.webhook').skipCsrf()

// Auth overrides — user routes win over storage/framework/defaults/
// routes/dashboard.ts's copies. Same login/2FA/logout flow, plus
// mirroring the bearer into an HttpOnly cookie so the server-rendered
// dashboard (resources/views/dashboard/*.stx) can resolve the current
// user/team during SSR — see Actions/Auth/LoginAction's doc comment.
route.post('/login', 'Actions/Auth/LoginAction').rateLimit(5, 'minute')
route.post('/register', 'Actions/Auth/RegisterAction').rateLimit(3, 'minute')
route.post('/verify-two-factor-login', 'Actions/Auth/VerifyTwoFactorLoginAction').rateLimit(10, 'minute')
route.post('/logout', 'Actions/Auth/LogoutAction').middleware('auth')

// Single sign-on via OIDC (authorization code + PKCE). GET on purpose —
// both legs are full-page browser navigations driven by the IdP, not
// XHR calls, so they live outside the SPA token flow entirely and mint
// the same HttpOnly cookie session LoginAction does. Providers are
// configured per install in config/sso.ts (Google, Okta, Entra ID, or
// any spec-compliant issuer via the generic entry).
route.get('/auth/sso/{provider}', 'Actions/Auth/SsoRedirectAction').rateLimit(10, 'minute')
route.get('/auth/sso/{provider}/callback', 'Actions/Auth/SsoCallbackAction').rateLimit(10, 'minute')

