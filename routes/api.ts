// No route.get('/', ...) here: static views win over programmatic routes
// at the same path in this router, so resources/views/index.stx always
// handles '/' regardless of what's registered here. Custom status page
// domains (stacksjs/status#1 Phase 7) are resolved inside that view's
// own <script server> block instead — see the comment at the top of
// resources/views/index.stx for why.

// Monitor CRUD (index/show/update/destroy) is auto-generated by the
// `useApi` trait on app/Models/Monitor.ts. `store` is overridden here
// (user-defined routes take priority over the auto-generated ones) to
// enforce the team's plan limit before creating — see
// Actions/Monitors/CreateMonitorAction. Custom action: triggering an
// out-of-band check outside the scheduler's normal cadence.
route.post('/monitors', 'Actions/Monitors/CreateMonitorAction')
route.post('/monitors/{id}/check', 'Actions/Monitors/RunCheckAction')

// Incident CRUD (index/store/show/update) is auto-generated by the
// `useApi` trait on app/Models/Incident.ts. Custom action: a one-step
// acknowledge, since the auto-generated `update` route requires PATCHing
// the full resource with a status string.
route.post('/incidents/{id}/acknowledge', 'Actions/Incidents/AcknowledgeIncidentAction')

// TeamMember index/show/destroy is auto-generated by the `useApi` trait on
// app/Models/TeamMember.ts. Custom actions: creating a member is always an
// invite (pending until accepted), and acceptance links a real user id.
route.post('/teams/{id}/invite', 'Actions/Teams/InviteTeamMemberAction')
route.post('/team-invites/{uuid}/accept', 'Actions/Teams/AcceptTeamInviteAction')

// Browser-facing invite acceptance: the invite email links to the page at
// resources/views/invite/[uuid].stx, whose form posts here. Unlike the
// JSON accept above it links-or-registers the user, activates the
// membership, and mints a session cookie (see AcceptInviteFormAction).
// Rate-limited: the uuid is a bearer capability, so throttle guessing.
route.post('/invite-forms/{uuid}/accept', 'Actions/Teams/AcceptInviteFormAction').rateLimit(10, 'minute')

// Public heartbeat ping endpoint (no auth — the token itself is the secret).
// GET *and* POST both work since cron jobs commonly just `curl` the URL.
// .skipCsrf() on the POST variant: this is hit by external cron jobs/curl,
// never a browser form post, so it can never carry a CSRF token — without
// this it 403s "CSRF token mismatch" on every POST (confirmed broken as of
// stacksjs/status#1, discovered while adding the metrics endpoint below).
route.get('/ping/{token}', 'Actions/Heartbeats/ReceivePingAction')
route.post('/ping/{token}', 'Actions/Heartbeats/ReceivePingAction').skipCsrf()

// Public agent metrics ingest endpoint (no auth — metrics_token IS the
// auth, same unguessable-token pattern as /ping/{token} above). A
// customer's own monitored server pushes CPU/RAM samples here on its own
// cron cadence; we don't poll for this data, it's push-only. Same
// .skipCsrf() reasoning as the ping POST route above.
route.post('/agent/{token}/metrics', 'Actions/Agents/ReceiveMetricsAction').skipCsrf()

// Regional probe fleet (stacksjs/status#1 Phase 11, push-probe topology).
// A remote check region (e.g. the Ashburn/us-east worker) has no shared DB:
// it pulls the monitors to probe, runs the checks locally, and POSTs the
// region-tagged results back here, where EvaluateMonitorConsensus weighs
// them for cross-region agreement. Auth is the unguessable
// REGIONAL_INGEST_TOKEN in the URL — same token-is-the-secret pattern as
// /ping and /agent above; .skipCsrf() for the same server-to-server reason.
// GET /status is read-only introspection used to verify the fleet e2e.
route.get('/regions/{token}/monitors', 'Actions/Regions/ListRegionMonitorsAction')
route.post('/regions/{token}/results', 'Actions/Regions/IngestRegionResultsAction').skipCsrf()
route.get('/regions/{token}/status', 'Actions/Regions/RegionStatusAction')

// StatusPage CRUD (index/show/update/destroy) is auto-generated by the
// `useApi` trait on app/Models/StatusPage.ts. `store` is overridden here,
// same pattern as CreateMonitorAction, to enforce the team's plan limit.
route.post('/status-pages', 'Actions/StatusPages/CreateStatusPageAction')

// One-time migration from an existing Better Stack/Instatus/Statuspage.io
// page (stacksjs/status#1 Phase 12).
route.post('/status-pages/import', 'Actions/StatusPages/ImportStatusPageAction')

// StatusReportUpdate index/show is auto-generated by the `useApi` trait on
// app/Models/StatusReportUpdate.ts. `store` is overridden here because the
// generated store handler inserts via raw db writes that fire no model
// events — posting an update must emit 'statusreportupdate:created' so
// status page subscribers get emailed (stacksjs/status#1 Phase 12
// follow-up; see Actions/StatusPages/CreateStatusReportUpdateAction).
route.post('/status-report-updates', 'Actions/StatusPages/CreateStatusReportUpdateAction')

// Password/email-domain gate for access-controlled status pages
// (stacksjs/status#1 Phase 12) — public, no auth (this route IS the auth).
route.post('/status/{slug}/unlock', 'Actions/StatusPages/UnlockStatusPageAction')

// Status page subscriber signup/unsubscribe — public, no auth.
route.post('/status/{slug}/subscribe', 'Actions/StatusPages/SubscribeAction')
route.get('/status/{slug}/unsubscribe/{token}', 'Actions/StatusPages/UnsubscribeAction')
route.get('/status/{slug}/feed', 'Actions/StatusPages/IncidentFeedAction')

// Dashboard status-page builder form posts (stacksjs/status#1 Phase 8) —
// plain POST + redirect, not the JSON-returning API actions above, since
// these back native HTML <form> elements with no client JS (see
// Actions/StatusPages/UpdateStatusPageAction for why). Deliberately NOT
// under /dashboard/status-pages/* — that collides with the dynamic file
// route resources/views/dashboard/status-pages/[id].stx (e.g. a POST to
// .../create matches [id]="create" as a GET-only view route first and
// 405s) — same "static/dynamic views win over routes at the same path"
// behavior documented at the top of this file, just for a dynamic [id]
// route instead of a static one this time.
route.post('/status-page-forms/create', 'Actions/StatusPages/DashboardCreateStatusPageAction')
route.post('/status-page-forms/{id}/update', 'Actions/StatusPages/UpdateStatusPageAction')
route.post('/status-page-forms/{id}/monitors/add', 'Actions/StatusPages/DashboardAssignMonitorAction')
route.post('/status-page-forms/{id}/monitors/remove', 'Actions/StatusPages/DashboardRemoveMonitorAction')

// Dashboard notification-channel form posts (stacksjs/status#1 Phase 8) —
// same plain-POST convention, and same /*-forms/ naming to dodge the
// dynamic-view-collision explained above (there's no [id].stx under
// dashboard/settings, but keeping the convention consistent avoids
// having to remember which pages need the workaround).
route.post('/notification-channel-forms/create', 'Actions/Notifications/DashboardCreateChannelAction')
route.post('/notification-channel-forms/{id}/test-send', 'Actions/Notifications/DashboardTestSendAction')
route.post('/notification-channel-forms/monitors/{monitorId}/assign', 'Actions/Notifications/DashboardAssignChannelAction')
route.post('/notification-channel-forms/monitors/{monitorId}/remove', 'Actions/Notifications/DashboardRemoveChannelAction')

// Dashboard team settings form posts (stacksjs/status#1 Phase 8) — same
